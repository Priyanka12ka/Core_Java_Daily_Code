/*Q14. Write a Java program to swap two numbers using a third variable.
 Input: A = 5
           B = 10
 */
public class PQue10 {
    public static void main(String[] args) {
        int temp;
        int a=10;
        int b=77;
        temp=a;
        a=b;
        b=temp;
        System.out.println("a: "+a);
        System.out.println("b: "+b);
    }

}
